def text_to_utf8_binary(text):
    utf8_bytes = text.encode('utf-16')
    binary_result = ''.join(format(byte, '08b') for byte in utf8_bytes)
    return binary_result

def utf8_binary_to_text(binary_text):
    bytes_array = [int(binary_text[i:i+8], 2) for i in range(0, len(binary_text), 8)]
    utf8_bytes = bytes(bytes_array)
    decoded_text = utf8_bytes.decode('utf-16')
    return decoded_text

# Пример использования
input_text = "привееееет"
utf8_binary_text = text_to_utf8_binary(input_text)
print(f"Текст в бинарном формате UTF-8: {utf8_binary_text}")

decoded_text = utf8_binary_to_text(utf8_binary_text)
print(f"Обратно из бинарного в текст: {decoded_text}")




